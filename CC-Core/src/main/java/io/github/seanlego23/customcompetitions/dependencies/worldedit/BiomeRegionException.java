package io.github.seanlego23.customcompetitions.dependencies.worldedit;

public class BiomeRegionException extends Exception {

    public BiomeRegionException() {
        super();
    }

    public BiomeRegionException(String message) {
        super(message);
    }

    public BiomeRegionException(String message, Throwable cause) {
        super(message, cause);
    }

    public BiomeRegionException(Throwable cause) {
        super(cause);
    }

    protected BiomeRegionException(String message, Throwable cause, boolean enableSuppression,
            boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
